async function decryptNcmFile(file) {
  const buf = await file.arrayBuffer();
  const dv = new DataView(buf);
  const u8 = new Uint8Array(buf);
  let off = 0;

  if (dv.getUint32(0, true) !== 0x4E455443 || dv.getUint32(4, true) !== 0x4D414446)
    throw new Error('不是有效的 NCM 文件');
  off = 10;

  const keyLen = dv.getInt32(off, true); off += 4;
  const keyData = u8.subarray(off, off + keyLen); off += keyLen;
  for (let i = 0; i < keyLen; i++) keyData[i] ^= 0x64;
  const decKey = aes128EcbDecrypt(coreKey, keyData);
  const rc4Key = decKey.subarray(17);
  const lookup = ncmBuildLookup(rc4Key);

  const metaLen = dv.getInt32(off, true); off += 4;
  let format = '';
  if (metaLen > 0) {
    const metaEnc = u8.subarray(off, off + metaLen); off += metaLen;
    for (let i = 0; i < metaLen; i++) metaEnc[i] ^= 0x63;
    const b64 = String.fromCharCode(...metaEnc.subarray(22));
    const metaRaw = Uint8Array.from(atob(b64), c => c.charCodeAt(0));
    const decMeta = aes128EcbDecrypt(modifyKey, metaRaw);
    const json = new TextDecoder().decode(decMeta.subarray(6));
    try { const m = JSON.parse(json); if (m.format) format = m.format; } catch {}
  }

  off += 5;
  const coverFrameLen = dv.getInt32(off, true); off += 4;
  dv.getInt32(off, true); off += 4;
  off += coverFrameLen;

  const audioEnc = new Uint8Array(buf, off);
  const audioDec = new Uint8Array(audioEnc.length);
  for (let i = 0; i < audioEnc.length; i++)
    audioDec[i] = audioEnc[i] ^ lookup[i & 0xff];

  let ext = format;
  if (!ext) {
    if (audioDec[0]===0x49 && audioDec[1]===0x44 && audioDec[2]===0x33) ext = 'mp3';
    else if (audioDec[0]===0x66 && audioDec[1]===0x4C && audioDec[2]===0x61 && audioDec[3]===0x43) ext = 'flac';
    else if (audioDec[0]===0x52 && audioDec[1]===0x49 && audioDec[2]===0x46 && audioDec[3]===0x46) ext = 'wav';
    else if (audioDec[0]===0x4F && audioDec[1]===0x67 && audioDec[2]===0x67 && audioDec[3]===0x53) ext = 'ogg';
    else ext = 'mp3';
  }

  const mime = ext==='mp3'?'audio/mpeg':ext==='flac'?'audio/flac':ext==='wav'?'audio/wav':ext==='ogg'?'audio/ogg':'audio/mpeg';
  return { blob: new Blob([audioDec], {type:mime}), ext, name: file.name.replace(/\.ncm$/i, '') };
}

async function startDecrypt() {
  const files = _decryptFiles;
  if (files.length === 0) { addLog('请先添加 NCM 文件', 'warn'); return; }
  const btn = document.getElementById('startDecrypt');
  btn.disabled = true; btn.textContent = '解密中...';
  addLog(`开始解密 ${files.length} 个文件`, 'info');
  let ok = 0, err = 0;
  for (const f of files) {
    try {
      const result = await decryptNcmFile(f);
      downloadBlob(result.blob, result.name + '.' + result.ext);
      addLog(`${f.name} → ${result.name}.${result.ext}`, 'info');
      ok++;
    } catch (e) {
      addLog(`${f.name} 解密失败: ${e.message}`, 'error');
      err++;
    }
  }
  addLog(`完成: ${ok} 成功, ${err} 失败`, err > 0 ? 'warn' : 'info');
  saveStats('decrypt', ok);
  btn.disabled = false; btn.textContent = '开始解密';
}

