const Rcon = [0,1,2,4,8,16,32,64,128,27,54,108,216];

function gfMul(a, b) {
  let p = 0;
  for (let i = 0; i < 8; i++) {
    if (b & 1) p ^= a;
    const carry = a & 0x80;
    a = (a << 1) & 0xff;
    if (carry) a ^= 0x1b;
    b >>= 1;
  }
  return p;
}

function aes128EcbDecrypt(key, ct) {
  const w = new Uint32Array(44);
  for (let i = 0; i < 4; i++)
    w[i] = (key[4*i]<<24)|(key[4*i+1]<<16)|(key[4*i+2]<<8)|key[4*i+3];
  for (let i = 4; i < 44; i++) {
    let t = w[i-1];
    if (i % 4 === 0)
      t = (Sbox[(t>>16)&255]<<24)^(Sbox[(t>>8)&255]<<16)^(Sbox[t&255]<<8)^Sbox[t>>>24] ^ (Rcon[i/4]<<24);
    w[i] = w[i-4] ^ t;
  }

  function addRoundKey(s, round) {
    for (let c = 0; c < 4; c++) {
      const kw = w[round*4+c];
      s[c*4]   ^= (kw>>24)&255; s[c*4+1] ^= (kw>>16)&255;
      s[c*4+2] ^= (kw>>8)&255;  s[c*4+3] ^= kw&255;
    }
  }

  const pt = new Uint8Array(ct.length);
  for (let off = 0; off < ct.length; off += 16) {
    const s = new Uint8Array(ct.subarray(off, off+16));
    addRoundKey(s, 10);
    for (let r = 9; r >= 1; r--) {
      for (let i = 0; i < 16; i++) s[i] = InvSbox[s[i]];
      {
        const t = s[1]; s[1]=s[13]; s[13]=s[9]; s[9]=s[5]; s[5]=t;
        const ta=s[2],tb=s[6]; s[2]=s[10]; s[6]=s[14]; s[10]=ta; s[14]=tb;
        const tc=s[3]; s[3]=s[7]; s[7]=s[11]; s[11]=s[15]; s[15]=tc;
      }
      addRoundKey(s, r);
      for (let c = 0; c < 4; c++) {
        const i=c*4, a=s[i], b=s[i+1], cc=s[i+2], d=s[i+3];
        s[i]=gfMul(14,a)^gfMul(11,b)^gfMul(13,cc)^gfMul(9,d);
        s[i+1]=gfMul(9,a)^gfMul(14,b)^gfMul(11,cc)^gfMul(13,d);
        s[i+2]=gfMul(13,a)^gfMul(9,b)^gfMul(14,cc)^gfMul(11,d);
        s[i+3]=gfMul(11,a)^gfMul(13,b)^gfMul(9,cc)^gfMul(14,d);
      }
    }
    for (let i = 0; i < 16; i++) s[i] = InvSbox[s[i]];
    {
      const t = s[1]; s[1]=s[13]; s[13]=s[9]; s[9]=s[5]; s[5]=t;
      const ta=s[2],tb=s[6]; s[2]=s[10]; s[6]=s[14]; s[10]=ta; s[14]=tb;
      const tc=s[3]; s[3]=s[7]; s[7]=s[11]; s[11]=s[15]; s[15]=tc;
    }
    addRoundKey(s, 0);
    pt.set(s, off);
  }

  const pad = pt[pt.length-1];
  if (pad > 0 && pad <= 16) return pt.subarray(0, pt.length - pad);
  return pt;
}

function ncmBuildLookup(rc4Key) {
  const s = new Uint8Array(256);
  for (let i = 0; i < 256; i++) s[i] = i;
  let c = 0, lastByte = 0, keyOffset = 0;
  for (let i = 0; i < 256; i++) {
    const swap = s[i];
    c = (swap + lastByte + rc4Key[keyOffset]) & 0xff;
    keyOffset = (keyOffset + 1) % rc4Key.length;
    s[i] = s[c];
    s[c] = swap;
    lastByte = c;
  }
  const lookup = new Uint8Array(256);
  for (let pos = 0; pos < 256; pos++) {
    const j = (pos + 1) & 0xff;
    lookup[pos] = s[(s[j] + s[(s[j] + j) & 0xff]) & 0xff];
  }
  return lookup;
}

